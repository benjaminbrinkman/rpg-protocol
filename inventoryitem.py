class InventoryItem:
    """The InventoryItem class provides item data for an entity's inventory"""
    
    def __init__(item, quantity):
        self.item = item
        self.quantity = quantity
        
    def add(i):
        self.quantity += i
    
    def remove(i):
        self.quantity -= i
    
    def setQuantity(i):
        self.quantity = i
